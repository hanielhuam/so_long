#include "so_long.h"

void	whereis(char c, int *i, int *j, char **board)
{
	if (!i || !j)
		return ;
	*i = 0;
	while (board[*i])
	{
		*j = 0;
		while (board[*i][*j])
		{
			if (board[*i][*j] == c)
				return ;
			(*j)++;
		}
		(*i)++;
	}
	*i = -1;
	*j = -1;
}
